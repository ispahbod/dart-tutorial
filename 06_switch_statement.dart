void main() {
  // Example: Switch Statement
  var num = 2 + 2;

  switch (num) {
    case 2:
      print(2); // If num is 2, print 2
      break;
    case 3:
      print(3); // If num is 3, print 3
      break;
    default:
      print('default'); // If num doesn't match any case, print 'default'
      break;
  }
}
